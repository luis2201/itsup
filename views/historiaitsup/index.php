        <div class="row" style="background-color:#ddd">
            <div class="card col-lg-8 col-md-8 col-sm-8 p-3 text-justify border border-0" style="margin:auto">
                <p>El Instituto Superior Tecnológico Portoviejo con condición Superior Universitario fue creado por el Ministerio de Educación, mediante 
                Acuerdo # 2755 del 18 de junio de 1996, con las especializaciones de Contabilidad Bancaria y Análisis de Sistemas, a funcionar a partir 
                del periodo lectivo: 1996-1997. Contó en un inició con 123 estudiantes, 11 docentes, tres administrativos y un encargado en servicios generales.</p>
            
                <p>En el año 1998 se crea la carrera de Administración de Empresas, en la que se graduaron dos promociones. Posteriormente, se crea la carrera de 
                Electrónica - Informática mediante el Acuerdo Ministerial N° 1915 del 20 de marzo de 2002, y las carreras de Hotelería y Turismo y Secretariado 
                Gerencial con el Acuerdo del CONESUP N° 140 del 26 de septiembre de 2003.</p>

                <p>El Consejo Nacional de Educación Superior (CONESUP), de conformidad con la Ley de Educación Superior del año 2000, asumió el control de los 
                institutos superiores técnicos y tecnológicos del país, y emitió el Registro Institucional No. 13-002 en el que reconocía al ITSUP dentro del 
                sistema nacional de educación superior.</p>

                <p>El instituto, como institución de educación superior y de derecho privado, desde sus inicios afianzó su objeto social de servicio sin fines de 
                lucro, donde prima su capacidad de autogestión administrativa y financiera para el cumplimiento de su misión y patrimonio propio.</p>

                <p>El instituto se rige por las normas del ordenamiento jurídico ecuatoriano, particularmente por la Constitución Política, la Ley Orgánica de 
                Educación Superior y su Reglamento, el Reglamento General de los Institutos Superiores, el Estatuto Institucional y por sus Reglamentos 
                Internos. Forma parte de los Sistema Nacional de Educación Superior, Evaluación y Acreditación, Ciencia y Tecnología, Nacional de Admisión y 
                Nivelación y Planificación de la Educación Superior.</p>

                <p>Se ubica en la provincia de Manabí, cantón Portoviejo, parroquia 12 de marzo, con tiempo de vida institucional indefinida.</p>

                <p>Una vez posicionado en el contexto provincial y local; ante el pedido de muchos padres de familia, decidió en el año 1999 crear la sección 
                bachillerato, con la misma razón social, asumiéndose las  especialidades: Físico-Matemática, Químico- Biológica e Informática; dentro del 
                bachillerato en Ciencias, tal como lo contempló el Acuerdo Ministerial emitido por la Sub-secretaría Regional de Educación, con fecha 11 de 
                marzo de 1999, entrando a funcionar a partir del año lectivo 1999 – 2000 con el primer año de bachillerato, año lectivo 2000 – 2001 con 
                primero y segundo de bachillerato, y el año lectivo 2001 – 2002 con los tres niveles de bachillerato; obteniendo en esta fecha su primera 
                promoción en la Especialidad de Informática.</p>

                <p>En cuanto a sus autoridades, desde su creación hasta diciembre del año 2011, tanto el colegio como el instituto estuvieron rectorados por el 
                Dr. Roberth Zambrano Santos.</p>

                <p>En sus inicios el instituto sesionaba en un inmueble rentado, a medida que se iba desarrollando, fue adquiriendo bienes patrimoniales a 
                inicios de los años 2000 y en la primera década logró la tenencia de terrenos donde se construyeron tres edificios. La infraestructura ha 
                sido prioridad en virtud del mejoramiento de la calidad en los servicios educativos.</p>

                <p>El instituto ha formado parte de la historia del Ecuador, de la región costanera y del cantón; así como la historia ha incidido en su quehacer 
                académico, investigativo y de vinculación con la sociedad.</p>

                <p>El 16 de abril de 2016, con apenas 75 segundos; un sismo de magnitud 7.8 en la escala de Richter estremeció al Ecuador. Entre daños, decesos 
                y heridos; se marcó una tragedia que quedó sembrada en la memoria de los sobrevivientes, el instituto perdió parte de su infraestructura y 
                tuvo ante sí, dos opciones: abandonar la obra o reconquistarse. Toda la familia educativa se puso de acuerdo y decidió la segunda opción; 
                que implicó secarse las lágrimas y asumir el reto de luchar por la casa institucional.</p>

                <p>Las 3.430 réplicas luego de aquel suceso, no permitían un pensamiento claro, a pesar de la confusión el espíritu de lucha, de supervivencia 
                y el amor a la educación hizo del instituto una familia mejor. El camino se hizo complejo, duro, difícil y angosto; entre escombros y sollozos 
                comenzó la recuperación, que no solo fue en el orden constructivo y material, sino en lo emocional, esperanzador y entusiasta de nuevas netas.</p>

                <p>Los estudiantes se reinsertaron, había temor, pero el cuidado y la atención de los docentes propició a la seguridad y la conquista de una 
                educación cualitativamente superior. No hubo forma de detenerse, así que se consolidaron las carreras existentes y se obtuvo la condición 
                de instituto acreditado en el año 2016.</p>

                <p>Posteriormente, se incrementó la oferta académica con la introducción de la Tecnología Superior en: Secretariado Ejecutivo 
                (RPC-SO-12-No.173-2018), Electrónica (RPC-SE-04-No.013-2018), Desarrollo de Software (RPC-SO-20-No.297-2018), Turismo (RPC-SE-94-No.13-2018), 
                Enfermería (RPC-SO-26-No.417-2018), Emergencias Médicas (RPC-SO-02-No.007-2019); Rehabilitación Física (RPC-SO-10-No.185-2020), 
                Educación Inclusiva (RPC-SO-22-No.555-2021 ), todas debidamente aprobadas por el Consejo de Educación Superior (CES).</p>

                <p>El 26 de octubre de 2020 el Consejo de Educación Superior aprobó el informe final para adquirir la condición de superior universitario 
                remitido por la Comisión Permanente de Institutos y Conservatorios Superiores mediante memorando CES-CPIC-2020-0629-M, otorgando de esta 
                manera la Condición de Superior Universitario a nuestra institución.</p>

                <p>La prioridad constante han sido las innovaciones pedagógicas implementadas en el proceso de enseñanza-aprendizaje; las que constituyen una 
                herramienta operativa que permite a la institución asumir estrategias, a fin de aportar con soluciones creativas a los problemas del contexto 
                sociocultural, garantizando la calidad educativa en los diferentes niveles de educación que oferta.</p>

                <p>La esencia del ITSUP se fundamenta en la transferencia y construcción del conocimiento, el mismo que es revisado y cuestionado entre pares 
                académicos. La noción de pertinencia implica conciencia e intencionalidad y, por tanto, remite al sujeto en condición de conocimiento y capacidad 
                de dominio sobre las circunstancias que le envuelve.</p>

                <p>La pertinencia social representa para la institución, el mayor grado de compromiso e involucramiento, donde se establece un nexo entre las 
                necesidades y expectativas de la sociedad. Sobre esta base, el instituto busca asentarse como una institución de excelencia, donde se desarrolle 
                la creatividad y la innovación, con capacidad para formar profesionales de alto nivel y ciudadanos útiles para el país; además de mantener el 
                reconocimiento como una institución con altos niveles de calidad y punto de referencia en el ámbito internacional, nacional, regional, 
                provincial y cantonal en la enseñanza e innovación tecnológica.</p>

                <p>La institución se encuentra bajo las directrices de las autoridades pertinentes, quienes propician que el instituto esté comprometido con la 
                elevación de los niveles de calidad en la educación con el propósito de entregar a la sociedad profesionales con conocimientos, habilidades, 
                destrezas y valores necesarios para que puedan enfrentar las exigencias del mercado laboral con visión de futuro, de acuerdo a los adelantos 
                de la ciencia y la tecnología.</p>

                <p>La educación es una herramienta fundamental de progreso, por tanto, es necesario reafirmar una profunda convicción de respeto a la función 
                esencial, en el desarrollo, formación continua y permanente del estudiante a través de la integración e interrelación docente, investigativa 
                y de vinculación con la comunidad; las que sirven de guía en la concreción del servicio de la sociedad para hacer retroceder la pobreza, la 
                destrucción de valores y la desigualdad social.</p>
